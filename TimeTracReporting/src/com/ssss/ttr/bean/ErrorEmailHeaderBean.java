package com.ssss.ttr.bean;

public class ErrorEmailHeaderBean {
	
	String envLoc = null;
	String errorFromEmail = null;
	String errorToEmail = null;
	String errorSubject = null; 
	String errorSmtpHost = null; 

	
	public String getEnvLoc() {
		return envLoc;
	}
	public void setEnvLoc(String envLoc) {
		this.envLoc = envLoc;
	}
	public String getErrorFromEmail() {
		return errorFromEmail;
	}
	public void setErrorFromEmail(String errorFromEmail) {
		this.errorFromEmail = errorFromEmail;
	}
	public String getErrorToEmail() {
		return errorToEmail;
	}
	public void setErrorToEmail(String errorToEmail) {
		this.errorToEmail = errorToEmail;
	}
	public String getErrorSubject() {
		return errorSubject;
	}
	public void setErrorSubject(String errorSubject) {
		this.errorSubject = errorSubject;
	}
	public String getErrorSmtpHost() {
		return errorSmtpHost;
	}
	public void setErrorSmtpHost(String errorSmtpHost) {
		this.errorSmtpHost = errorSmtpHost;
	}
	
}
